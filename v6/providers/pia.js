function getInfrastructure() {
    const providerId = "pia";
    const openVPN = {
        moduleType: "OpenVPN",
        presetIds: {
            recommended: "default"
        }
    };

    const raw = getText("https://serverlist.piaservers.net/vpninfo/servers/v6");
    let json = null;
    try {
        if (!raw.response) {
            throw "network";
        }
        const rawLines = raw.response.split("\n");
        if (rawLines.length == 0) {
            throw "parsing";
        }
        json = JSON.parse(rawLines[0]);
        if (!json) {
            throw "parsing"
        }
    } catch (error) {
        return {
            error: error
        };
    }

    const servers = [];
    json.regions.forEach(v => {
        const hostname = v.dns;
        const id = hostname.split('.')[0];
        const country = v.country;

        const server = {
            serverId: id,
            hostname: hostname,
            supportedModuleTypes: [openVPN.moduleType]
        };
        const metadata = {
            providerId: providerId,
            countryCode: country,
            categoryName: "Default"
        };
        metadata.area = v.name;
        server.metadata = metadata;

        servers.push(server);
    });

    const presets = getOpenVPNPresets(providerId, openVPN.moduleType, openVPN.presetIds, json.groups);

    return {
        response: {
            presets: presets,
            servers: servers,
            cache: json.cache
        }
    };
}

